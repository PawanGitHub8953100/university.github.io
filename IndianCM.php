<p id="c_m">Yogi Adityanath (born Ajay Mohan Singh Bisht; 5 June 1972)[6][1][7][a][9] is an Indian Hindu monk and politician from the Bharatiya Janata Party who is serving as the 21st and current Chief Minister of Uttar Pradesh since 19 March 2017. He is the longest serving Chief Minister of Uttar Pradesh, who is currently running his tenure for over 6 years, surpassing Sampurnanand.[10] He represents Gorakhpur Urban Assembly constituency in the Uttar Pradesh Legislative Assembly since 2022 and was member of the Uttar Pradesh Legislative Council from 2017 to 2022. He is a former Member of Parliament, Lok Sabha from Gorakhpur Lok Sabha constituency, Uttar Pradesh from 1998 to 2017 before he resigned to become the Chief Minister.[11] He resigned from the legislative council after being elected to the legislative assembly.

Adityanath is also the mahant (Head Priest) of the Gorakhnath Math, a Hindu monastery in Gorakhpur, a position he has held since September 2014 following the death of Mahant Avaidyanath, his spiritual "father".[12] He is also the founder of Hindu Yuva Vahini, a Hindu nationalist organisation.[13][14] He has an image of a Hindutva nationalist and a social conservative.[1][15][16][17][18]

Early life and education
Yogi Adityanath was born as Ajay Mohan Singh Bisht on 5 June 1972 in the village of Panchur, in Pauri Garhwal, Uttar Pradesh (now in Uttarakhand) in a Rajput family.[1][7][19][20][21] His late father, Anand Singh Bisht, was a forest ranger.[b] He was the second born in the family, among four brothers and three sisters.[24] He completed his bachelor's degree in mathematics from the Hemwati Nandan Bahuguna Garhwal University in Uttarakhand.[25][26]

He left his home around the 1990s to join the Ayodhya Ram temple movement. Around that time, he also became a disciple of Mahant Avaidyanath, the chief of the Gorakhnath Math.[25] Mahant Avaidyanath was leading the Ayodhya Ram temple movement at that time. While based in Gorakhpur after his initiation, Adityanath has often visited his ancestral village, establishing a school there in 1998.[24]

Adityanath was promoted to the rank of Mahant or high priest of the Gorakhnath Math after the death of Avaidyanath on 12 September 2014. He was made Peethadhishwar (Head Seer) of the Math amid traditional rituals of the Nath sect two days later.[27]

Early political career
Yogi Adityanath belongs to a specific tradition of Hindutva politics in Uttar Pradesh that can be traced back to the Mahant Digvijay Nath, who led the placing of idols in the Babri Masjid in Ayodhya on 22 December 1949.[28][29] Both Digvijay Nath and his successor Avaidyanath belonged to the Hindu Mahasabha and were elected to the Parliament on that party's ticket. After the BJP and the Sangh Parivar joined the Ayodhya movement in the 1980s, the two strands of Hindu nationalism came together. Avaidyanath switched to the BJP in 1991, but nevertheless maintained significant autonomy. Four years after Adityanath was designated Avaidyanath's successor, he was elected to the Lower House of the Indian Parliament (the Lok Sabha).[28]

After his first electoral win, Adityanath started his own youth organisation Hindu Yuva Vahini, which came to be known for its activities in the eastern Uttar Pradesh and was instrumental in Adityanath's meteoric rise. There have been recurrent tensions between Adityanath and the BJP leadership over the allocation of election tickets. However, the BJP has not let the tensions mount because Adityanath has served as a star campaigner for the party.[28][30][31]

In 2006, he took up links between Nepali Maoists and Indian Leftist parties as a key campaign issue and encouraged Madhesi leaders to oppose Maoism in Nepal.[13][32] In 2008, his convoy was reportedly attacked while en route to Azamgarh for an anti-terrorism rally. The attack left one person dead and at least six persons injured.[33][34]

In January 2007, Adityanath with other BJP leaders had gathered to mourn the death of a man who was killed because of religious violence. He and his supporters were subsequently arrested by the police and lodged in Gorkhapur jail on the charges of disturbing peace and violating prohibitory orders. His arrest led to further unrest during which several coaches of the Mumbai bound Mumbai-Gorakhpur Godan Express were burnt, allegedly by protesting Hindu Yuva Vahini activists.[35][36][37] The day after the arrest, the District Magistrate and the local police chief were transferred and replaced.[38]

Member of Parliament
When elected to the 12th Lok Sabha at age of 26, Adityanath was its youngest member. He was elected to the Parliament from Gorakhpur for five consecutive terms (in 1998, 1999, 2004, 2009 and 2014 elections).[9][39]

Adityanath's attendance in Lok Sabha was 77% and he has asked 284 questions, participated in 56 debates and introduced three private member Bills in the 16th Lok Sabha.[40]

Relations with the BJP
Adityanath has had strained relations with the BJP for more than a decade.[41] He often derided and undermined the BJP, criticising its dilution of the Hindutva ideology.[42] Having established his own independent power base in Eastern Uttar Pradesh, with the support of the Hindu Yuva Vahini and the Gorakhnath Math, he felt confident to be able to dictate terms to the BJP.[43] When his voice was not heard, he revolted by fielding candidates against the official BJP candidates. The most prominent example was the fielding of Radha Mohan Das Agarwal from Gorakhpur on a Hindu Mahasabha ticket in 2002, who then defeated BJP Cabinet minister, Shiv Pratap Shukla by a wide margin.[42] In 2007, Adityanath threatened to field 70 candidates for the state assembly against the BJP candidates. But he reached a compromise in the end.[44][45][46] In 2009 Parliamentary elections, Adityanath was rumoured to have campaigned against the BJP candidates who were then defeated.[42]

Despite his periodic revolts, Yogi Adityanath has been kept in good humour by the RSS and the BJP leaders.

Second term (2022–present)
Main article: Second Yogi Adityanath ministry
On 10 March 2022, with the announcement of the legislative assembly results, BJP-led NDA alliance secured 273 seats with Adityanath winning his second term. He and his party wrote history, being the first chief minister to return to power after completing a full 5-year term in office. BJP is also the first party to return to power consecutively after 37 years.[88][89] He was only the third chief minister, in Uttar Pradesh's political history to complete a full 5-year term as the chief minister of the state after Mayawati of BSP and Akhilesh Yadav belonging to the Samajwadi Party.[89]

In the buildup to the assembly elections, Adityanath successfully used a campaign with a bulldozer as its main image, earning him the nickname "Bulldozer Baba".[90][91] The term had initially been used as a taunt by an opposition party.[92] His speeches during the polls included hate speeches against Muslims, promoting religious polarization and Hindu supremacy. Further, his speeches included the idea that rights of Hindus are at odds with that of Muslims, where he repeatedly conflated Muslims with terrorists and criminals, and the opposition parties as appeasers of Muslims.[93]
